function add(a = 1, b = 1){
    return a + b

}
function subtract(a = 10, b = 5){
    return a - b
}
function multiply(a = 5, b = 5){
    return a * b
}
function divide(a = 10, b = 2){
    return a / b
}

function increment(n){
    return ++ (n)
}
function decrement(n){
    return -- (n)
}
function makeInt(n){
    return parseInt(n, 10);
}
function preserveDecimal(n){
    return parseFloat(n);
}